#!/bin/bash
set -e

# 切换到脚本所在目录（项目根目录）
cd "$(dirname "$0")"

echo "========================================"
echo "  TickList 全量测试开始"
echo "========================================"
echo ""

# ---- 后端测试 ----
echo "----------------------------------------"
echo "  [1/2] 运行后端测试 (pytest)"
echo "----------------------------------------"
cd backend
python -m pytest tests/ -v
cd ..
echo ""
echo "  ✅ 后端测试通过"
echo ""

# ---- 前端测试 ----
echo "----------------------------------------"
echo "  [2/2] 运行前端测试 (vitest)"
echo "----------------------------------------"
cd frontend
bun run test:run
cd ..
echo ""
echo "  ✅ 前端测试通过"
echo ""

# ---- 汇总 ----
echo "========================================"
echo "  🎉 所有测试全部通过！"
echo "========================================"
